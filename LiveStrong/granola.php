                            <div class="ingredients">
                                <h2>Ingredients</h2>
                                <br>
                                <ul>
                                    <li>400g jumbo Oats</li>
                                    <br>
                                    <li>juice 2 oranges(150ml), plus zest of ½</li>
                                    <br>
                                    <li>1 tsp ground cinnamon</li>
                                    <br>
                                    <li>2 tbsp freeze-dried raspberries or strawberries (see tip)</li>
                                    <br>
                                    <li>25g flaked almonds, toasted</li>
                                    <br>
                                    <li>25g mixed seeds (such as sunflower, pumpkin, sesame and linseed)</li>
                                    <br>
                                    <h3>To Serve</h3>
                                    <li>2 large oranges, peeled and segmented</li>
                                    <br>
                                    <li>mint leaves (optional)</li>
                                </ul>
                            </div>
                        </div>
                        
                        <div class="col-lg-8 col-md-4 col-sm-4">
			<div class="method">
                            <h2>Method</h2>
                            <h3>1.</h3><p>Put 200g oats and 500ml water in a food processor and blitz for 1 min. Line a sieve with 
                                clean muslin and pour in the oat mixture. Leave to drip through for 5 mins, then twist the ends of 
                                the muslin and squeeze well to capture as much of the oat milk as possible – it should be the consistency 
                                of single cream. Best chilled at least 1 hr before serving. Can be kept in a sealed or covered jug in the 
                                fridge for up to 3 days.</p>
				<h3>2.</h3><p>Heat oven to 200C/180C fan/gas 6 and line a baking tray with baking parchment. Put the orange 
                                    juice in a medium saucepan and bring to the boil. Boil rapidly for 5 mins or until the liquid has reduced 
                                    by half, stirring occasionally. Mix the remaining 200g oats with the orange zest and cinnamon. Remove the 
                                    pan from the heat and stir the oat mixture into the juice. Spread over the lined tray in a thin layer and 
                                    bake for 10-15 mins or until lightly browned and crisp, turning the oats every few mins. Leave to cool on the tray.</p>
				<h3>3.</h3><p>Once cool, mix the oats with the raspberries, flaked almonds and seeds. Can be kept in a sealed jar for up to one week. 
                                    To serve, spoon the granola into bowls, pour over the oat milk and top with the orange segments and mint leaves, if you like.</p>
