                                    <li>400g can red kidney beans in chilli sauce</li>
                                    <br>
                                    <li>400g can mixed pulses in water, rinsed and drained</li>
                                    <br>
                                    <li>2 tbsp tomato purée</li>
                                    <br>
                                    <li>1 tomato, finely chopped</li>
