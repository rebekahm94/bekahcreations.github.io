			<div class="method">
                            <h2>Method</h2>
                            <h3>1.</h3><p>Cook the rice according to the packet instructions then drain and keep warm.</p>
                            <h3>2.</h3><p>Meanwhile, make the wraps. Crack the eggs into a large measuring jug, whisk until smooth 
                            and season with a pinch of salt. Stir in the parsley.</p>
                            <h3>3.</h3><p>Spray a small non-stick frying pan with low calorie cooking spray and place over a high heat. 
                                When hot, pour in one-eighth of the egg mixture (or enough to just cover the bottom of the pan), tilting 
                                the pan to spread the egg around. Cook for 1-2 minutes or until lightly browned underneath then flip over 
                                and cook for another 1-2 minutes. Transfer the wrap to a baking sheet lined with baking parchment, cover 
                                with another sheet of parchment and keep warm. Spray the pan again with low calorie cooking spray and make 
                                another seven wraps in the same way, adding each cooked wrap to the pile.</p>
                            <h3>4.</h3><p>Heat a non-stick frying pan over a medium heat, add the kidney beans, pulses and tomato purée and 
                                warm through for 3-5 minutes. Remove from the heat and season to taste.</p>
                            <h3>5.</h3><p>Stir the beans into the rice and divide between the egg wraps. Scatter over the tomato, red onion and 
                                cucumber, and roll up loosely (you might need to hold the wraps in place with cocktail sticks).</p>
                            <h3>6.</h3><p>Serve hot with fat free natural Greek yogurt and lime wedges to squeeze over.</p>
